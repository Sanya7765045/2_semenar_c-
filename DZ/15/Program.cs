// Очищаем консоль
Console.Clear();

// Запрашиваем у пользователя цифру дня недели
Console.Write("Введите цифру дня недели: ");
int number = int.Parse(Console.ReadLine());

// Проверяем, что введенное число соответствует выходному дню (6 или 7)
if (number == 6 || number == 7)
{
    // Выводим сообщение, что это выходной день
    Console.WriteLine("Это выходной день.");
}
else
{
    // Выводим сообщение, что это не выходной день
    Console.WriteLine("Это не выходной день.");
}